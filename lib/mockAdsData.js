export function getMockAdsForSearch(keyword = '', network = 'all', activeOnly = true) {
  let filtered = [...MOCK_ADS];

  // Filter by keyword
  if (keyword) {
    const keywordLower = keyword.toLowerCase();
    filtered = filtered.filter(ad => 
      ad.copy_text.toLowerCase().includes(keywordLower) ||
      ad.brand_name.toLowerCase().includes(keywordLower)
    );
  }

  // Filter by network
  if (network !== 'all') {
    filtered = filtered.filter(ad => ad.network === network);
  }

  // Filter by active status
  if (activeOnly) {
    filtered = filtered.filter(ad => ad.is_active);
  }

  return {
    ads: filtered,
    meta: { success: true, count: filtered.filter(a => a.network === 'facebook' || a.network === 'instagram').length, error: null },
    tiktok: { success: true, count: filtered.filter(a => a.network === 'tiktok').length, error: null },
    total: filtered.length
  };
}
